	<span class="bg-primary flex-1 text-xl py-1">TRANSPORT</span>
	<div class="flex-1 bg-gray-100 text-secondary text-left ml-4 my-2">
		<p>
			Regelmatig krijgen we meldingen van poezen die verloren worden wanneer men er mee naar de
			dierenarts gaat. Een kat los in de wagen meenemen is om problemen vragen. Als ze schrikt bij
			het uitstappen kunt u ze niet vasthouden en loopt ze verloren in de vreemde omgeving. Erger
			nog als u een ongeval krijgt. Een kat moet steeds vervoerd worden in een degelijke
			transportkooi. De beste zijn de metalen draadkooien, ze zijn helaas praktisch niet meer
			verkrijgbaar. Gebruik zeker GEEN rieten manden, ze kunnen mooi ogen maar zijn absoluut
			onveilig. Vele plasticboxen hebben geen degelijke sluiting. Controleer ze, doe voor het
			deurtje een stuk triplex met een of 2 snelbinders rond. Moet u er nog een kopen kies dan bij
			voorkeur een "bovenlader" met degelijke sluiting. Daar nog een snelbinder rond en u kan op
			weg..
