	<div class="flex-1 bg-gray-100 text-secondary text-left ml-4 my-2">
		<p>
			Het knallen begint steeds vroeger en zijn dieren het slachtoffer. Dit willen we niet meemaken
			en toch gebeurt het jaarlijks.<br />
			LAAT UW DIEREN OP 31 DECEMBER NIET MEER BUITEN OOK OP 1 JANUARI WORDT ER NOG GEKNALD!<br />
			Laat uw hond enkel uit aan de leiband die u stevig vasthoudt en hou de wandeling kort. Van één
			bommetje kunnen ze schrikken, zich losrukken en verongelukken.<br />
			Neem hem niet mee naar een plaats waar vuurwerk wordt afgeschoten.<br />
			Zorg ervoor dat er iemand thuis blijft.<br />
			Sluit deuren, ramen, gordijnen, rolluiken en het kattenluik.<br />
			Het licht aanlaten en radio of TV laten spelen maskeert het ergste lawaai en licht.<br />
			Straf uw huisdier nooit voor een angstige reactie, maar troost het ook niet te veel. Dit werkt
			als een bevestiging van de vrees, waardoor de angst kan toenemen. Probeer het dier af te leiden
			met een spelletje.<br />
			Geef het ook de gelegenheid om ergens rustig weg te kruipen.<br />
			Voor extreem angstige dieren vraagt u uw dierenarts naar de therapeutische mogelijkheden.<br
			/>
			Weidedieren, zet u in een hok of op stal.<br />
			Een hok of volière kan eventueel afgedekt worden met een dik (brandvertragend) deken, om geluid
			en licht te dempen. Zorg wel voor een goede verluchting.<br />
			Controleer regelmatig of alles in orde is.<br />
			IN ANTWERPEN IS VUURWERK AFSCHIETEN OF HET BEZIT ERVAN, OOK VAN BOMMETJES, VERBODEN. ER STAAN ZWARE
			BOETES OP. Antwerpen verbiedt het afschieten van vuurwerk, ook het in bezit ervan zijn.<br />
			Beschik je over duidelijke foto's en filmpjes die kunnen bijdragen tot het identificeren van mogelijke
			verdachten? Dan kan je die uploaden via het
			<a class="text-primary" href="https://beelden.politie.antwerpen.be/vuurwerk2022."
				>beeldenportaal</a
			>. Betrapt met vuurwerk op zak? Dat kost je een pak geld!<br />
			Wie betrapt wordt met vuurwerk, zal nu ook de kosten van de verwerking moeten betalen. Zo riskeer
			je dus een GAS-boete en een retributiekost. De kosten bij het gebruik van (knal)vuurwerk kunnen
			dus hoog oplopen.<br />
			Wie door de politie betrapt wordt met vuurwerk op zak of op het gebruik ervan krijgt een GAS-boete.
			Maar er volgt ook nog een te betalen retributie. Dat bedrag dekt de kosten voor het onder meer
			veilig ophalen van het pyrotechnisch materiaal, het bewaren en de vernietiging ervan. Kosten:<br
			/>
			Wanneer de politie (knal)vuurwerk in beslag neemt tot 1 kilogram, inclusief de verpakking, volgt
			er op een doordeweekse dag een rekening van 250 euro. Van 1 tot 5 kg loopt dat bedrag al op naar
			450 euro. Neemt de politie meer in beslag, dan kost het diegene die betrapt werd 700 euro. In het
			weekend komt er sowieso nog 150 euro bij. Dus als je in het weekend betrapt wordt op het afsteken
			van een voetzoeker, dan krijg je een GAS-boete die tot 350 euro kan oplopen en daarbovenop nog
			een retributiekost van 400 euro.
