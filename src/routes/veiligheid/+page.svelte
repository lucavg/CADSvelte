			U kunt haken aanbrengen die binnenwaarts geplooid zijn en er geplastificeerde draad op aanbrengen.
			Sommige acrobaten zijn zo behendig dat ze recht tegenop een muur gaan, met de afrastering meedraaien
			en er zo over klimmen. Dit kan verhinderd worden door de draad breder te nemen dan de haken. Is
			de lengte van het gedeelte van de haak dat naar binnen staat bv 50 cm, dan neemt u draad van 100
			cm. Mochten ze proberen erover te raken is dit onmogelijk omdat de laatste 50 cm los is en naar
			beneden buigt wanneer ze erin kruipen. U kunt ook werken met buizen. Hieronder enkele voorbeelden.
			Om terrassen katveilig te maken zijn er netten in alle soorten, kleuren en maten. Er bestaan bevestigingen
