	<span class="bg-primary flex-1 text-xl py-1">Toxoplasmose</span>
	<div class="flex-1 bg-gray-100 text-secondary text-left ml-4 my-2">
		<p>
			Dit artikel werd voor ons gemaakt door professor P. Dorny, een autoriteit op het gebied van
			research, die hierover uitgebreid onderzoek deed en er zijn bijstelling over maakte. Omdat dit
			toch een zeer belangrijke kwestie is en er zoveel onvolledige of verkeerde informatie over
			bestaat, heeft hij alles nog eens samengevat voor u.
			<br /><br />
			Bij zwangerschap is het niet verantwoord een huiskat te verwijderen uit het gezin.
			<br />
			Daar wij op vrijwillige basis werken worden alle middelen rechtstreeks aangewend om de dieren te
			helpen: dure operaties, voeding enz. Voor lidmaatschap en steun aan de vereniging:<br />
			<span class="font-bold text-lg">Samenvatting</span><br />
			Een onderzoek naar de prevalentie van toxoplasma gondii bij 112 studenten diergeneeskunde toonde
			aan dat 47,3% antistoffen had. niettegenstaande het veelvuldig contact van deze mensen met katachtigen
			was de infectiegraad niet hoger dan in een niet geselecteerde populatie van dezelfde leeftijdsklasse
			in belgië. Aan de hand van een vragenlijst werd informatie bekomen over het houden van katten als
			huisdier en de eetgewoonten van deze personen. Daaruit bleek dat het veel werken in de tuin en
			het eten van rauw of weinig gebakken vlees en dan vooral schapenvlees, het risico op besmetting
			met toxoplasma verhoogt. Daarentegen waren eigenaars van katten niet frequenter besmet dan personen
			die geen kat bezitten.
			<br /><br />
			Toxoplasma gondii is ongetwijfeld één der meest verspreide parasieten: dit protozoön veroorzaakt
			infecties bij zowat alle zoogdieren en vogels over de ganse wereld. alhoewel de parasiet reeds
			lang gekend is, werd de cyclus ervan pas opgehelderd in 1970. Toen werd ontdekt dat de kat, als
			enige eindgastheer, toxoplasma onder vorm van oöcysten, met de uitwerpselen kan verspreiden. Vanaf
			dat moment werd het houden van een huiskat sterk afgeraden in gezinnen waar een vrouw zwanger was.
			Het schadelijk effect van een congenitale besmetting van de foetus was reeds voldoende gekend.
			<br /><br />
			De kat scheidt slechts gedurende een korte periode (1 à 2 weken) van zijn bestaan oöcysten uit
			en dit meestal op jonge leeftijd (jonger dan 1 jaar). Katten die oöcysten uitscheiden zijn meestal
			niet ziek en vertonen meestal geen diarree. Deze oöcysten zijn niet onmiddellijk infectieus maar
			worden dat pas na 2 à 5 dagen. daarentegen kunnen oöcysten in de buitenwereld wel zeer lang tot
			zelfs enkele jaren overleven. De kat is echter niet de enige besmettingsbron voor de mens; deze
			kan zich eveneens besmetten door het eten van rauw of onvoldoende gebakken vlees besmet met weefselcysten
			van toxoplasma. Vooral schapenvlees blijkt frequent besmet te zijn.
			<br /><br />
			Het doel van deze studie was enerzijds na te gaan of een geselecteerde populatiegroep van studenten
			diergeneeskunde, door het veelvuldig contact met katten frequenter besmet is met toxoplasma, en
			anderzijds aan de hand van een vragenlijst de besmettingswegen via het eten van vlees of via contact
			met katten te analyseren.
			<br /><br />
			De invloed van het contact met katten en kattenfaeces op de seroprevalentie van toxoplasma.
			<br /><br />
			Van de 57 personen die verklaarden één of meerdere katten te bezitten hadden 43,9% antistoffen.
			<br /><br />
			Bij de niet-kattenbezitters was dit 50,9%.
			<br /><br />
			Van de 19 personen die verklaarden veel in de tuin te werken waren er 73,7% seropositief.
			<br /><br />
			Hoewel niet significant, is er een duidelijke trend voor hogere besmettingsfrequenties bij personen
			die vlees tamelijk rauw nuttigen (56,6% positief) dan voor personen die vlees enkel goed gebakken
			eten (36% positief). Het eten van rauw of onvoldoende gebakken schapenvlees verhoogde zelfs aanzienlijk
			het risico (70,8%).
			<br /><br />
			Ondanks het veelvuldig contact van studenten diergeneeskunde met katachtigen bleken deze in het
			onderzoek niet frequenter besmet te zijn met toxoplasma gondii dan de doorsnee populatie in belgië.
			In een studie in Brussel bij 2.313 vrouwen tussen de 20 en 30 jaar oud, was 51,5% seropositief.
			In een onderzoek in Gent vond men dat op de leeftijd van 30 jaar reeds 3/4 van de bevolking met
			toxoplasma besmet was. Ook in buitenlandse onderzoekingen werden geen verschillen in seroprevalentie
			genoteerd tussen dierenartsen en niet-dierenartsen. Hoewel zij rechtstreeks in contact komen met
			katachtigen, komen ze minder in contact met de faeces. Daarbij komt dat de sporulatie van de oöcysten
			in de faeces minstens 2 à 5 dagen in beslag neemt, waardoor het contact met verse faeces ongevaarlijk
			is.
			<br /><br />
			Het houden van een huiskat impliceert meestal ook het schoonmaken van een kattenbak en daardoor
			ook eventueel contact met de kattenfaeces. Uit het huidig onderzoek bleek echter evenmin een verschil
			in seroprevalentie te bestaan tussen kattteneigenaars en zij die geen kat bezitten. In gelijkaardige
			studies in Europa en de Verenigde staten werden meestal geen verschillen in prevalentie tussen
			beide populatiegroepen gevonden. Nochtans is de besmettingsgraad met toxoplasma gondii bij huiskatten
			vrij hoog. Op de leeftijd van 2 jaar heeft tussen de 50 en 60% van de katten de infectie doorgemaakt.
			<br /><br />
			De reden van de geringe overdracht van de huiskat op de eigenaar moet gezocht worden in de hygiëne
			die in de meeste huizen onderhouden wordt. Kattenfaeces wordt meestal verwijderd vooraleer de oöcysten
			de kans gekregen hebben om te sporuleren (besmettelijk te zijn). Bovendien is het ook zo dat een
			kat die antistoffen gemaakt heeft in principe geen oöcysten meer uitscheidt. In tropische landen
			verhoogt het risico op besmetting met toxoplasma gondii. Het warme en vochtige klimaat zorgt er
			voor een snelle sporulatie van de oöcysten, bovendien zijn de huizen er meestal onhygiënisch. In
			Costa Rica bv. vond men in de huizen in 19,5% van de keukens en 15% achter versieringen resten
			van kattenfaeces. Op de leeftijd van 10 jaar zijn in dat land reeds meer dan 40% van de kinderen
			seropositief, terwijl infectie door weefselcysten er waarschijnlijk minder voorkomt vermits het
			vlees er meestal goed doorbakken gegeten wordt.
			<br /><br />
			De besmetting van het milieu is ook verantwoordelijk voor de hoge prevalentie van toxoplasma gondii
			bij herbivoren. Serologisch onderzoek bij Belgische runderen en schapen toonde aan dat respectievelijk
			72 en 80% van deze dieren positief zijn voor toxoplasma antistoffen. Varkens zijn minder frequent
			seropositief van 19 tot 50% en in de meeste westerse landen wordt een daling van de seroprevalentie
			waargenomen bij deze diersoort, waarschijnlijk door het meer gesloten houden van varkensbedrijven
			en het gebruik van silo's om het voeder op te stapelen, waardoor contaminatie met kattenfaeces
			onmogelijk wordt. In een uitgebreide studie in Frankrijk kwam men tot dezelfde vaststelling. Verhitten
			van vlees doodt de aanwezige weefselcysten, hiervoor zijn 15 à 20 minuten nodig bij een temperatuur
			van 60°c, en 5 minuten bij een temperatuur van 160 tot 170°c. Bij gewone koeltemperatuur (4°c)
			blijven de cysten minstens 3 weken actief. Bewaring aan -15° tot - 20° doodt alle weefselcysten.
			<br />
			<span class="font-bold text-lg">Besluit</span><br />
			Alhoewel de huiskat ook in gematigde streken zoals België ontegensprekelijk onmisbaar is in het
			sluiten van de cyclus van toxoplasma gondii, zijn humane besmettingen zelden afkomstig van rechtstreeks
			contact met deze diersoort.
			<br /><br />
			<span class="font-bold">Zwangere vrouwen houden rekening met:</span><br />
		</p>
		<ul class="list-disc mx-4">
			<li>
				Strikte hygiëne van de kattenbak. dagelijks verversen of laten verversen en ontsmetten.
				Oöcysten worden immers pas besmettelijk nadat ze 2 à 5 dagen in de buitenwereld zijn. Aan
				katten wordt best geen rauw vlees verstrekt.
			</li>
			<li>
				Een serologische (bloed) en eventueel coprologische (faeces) controle kan aantonen of de kat
				immuum is en geen oöcysten meer zal uitscheiden.
			</li>
			<li>
				Het werken in de tuin gebeurt met handschoenen, daarna de handen flink borstelen met water
				en (ontsmettings)zeep.
			</li>
			<li>Groenten steeds grondig reinigen vóór consumptie.</li>
			<li>
				Het risico van besmetting door vlees kan men beletten door het vlees voldoende te verhitten,
				maar ook door het te diepvriezen. Minstens 3 dagen bewaren aan -15 tot -20°c doodt alle
				weefselcysten. bij gewone koeltemperatuur (4°c) blijven de cysten minstens 3 weken actief.
			</li>
		</ul>
