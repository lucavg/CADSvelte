			<p>
				Een kat is zeer gevoelig voor allerlei veranderingen. Ze voelt perfect aan wanneer er iets
				op til is, dus ook een verhuis. Er is dan veel over en weer geloop, deuren staan open, poes
				zal zich onzeker voelen en trachten te ontsnappen. Grote oplettendheid is geboden. Zet ze
				vooraf in een kamer, hetzij in de oude of de nieuwe woning en doe de deur op slot tot alles
				terug rustig is en alle deuren gesloten blijven. Uiteraard moet poes minimum drie weken
				binnenblijven, tot ze alle geuren van haar nieuwe huis goed kent. Hebt u een tuin, ga er dan
				de eerste keren samen mee in de tuin. Speel met een balletje, koord zodat de poes alle
				geuren goed kan opsnuiven. Gooi een snoepje waar ze kan achter lopen om het te pakken. Roep
				ze regelmatig terug, ritsel met het zakje van snoepjes, schud met het droogvoer, of iets
				anders wat ze associeert met eten. Indien u de tuin kunt afsluiten met draad die naar binnen
				buigt of een ander systeem, is natuurlijk ideaal.
