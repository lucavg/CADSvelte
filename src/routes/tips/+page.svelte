		<span class="bg-primary flex-1 text-xl py-1">VAKANTIEPERIODE</span>
		<br />
		<div class="flex-1 bg-gray-100 text-secondary text-left ml-4 my-2">
			<p>
				<span class="font-bold">Belangrijk voor uw vertrek</span><br />
				Controleer uw huis, inclusief zolder, kelder, tuinhuisje, verluchting. Jaarlijks raken poezen
				opgesloten in een woning waar de mensen met vakantie zijn. U bespaart dieren veel leed en onaangename
				verrassingen bij uw terugkeer. Geef een sleutel aan familie, vrienden of buren. Het kan op velerlei
				gebied nuttig zijn. Verwittig uw buren wie ze kunnen contacteren ingeval van nood. U vertrekt
				met vakantie en uw verloren dier is nog niet terecht: waarschuw ons en geef het telefoonnummer
				van een contactpersoon.
				<br /><br />
				<span class="font-bold">Uw dier mag mee op vakantie</span><br />
				Vergeet niet de nodige documenten mee te nemen: het Europees paspoort, met inentingsdata, chipnummer
				en voor sommige bestemmingen een gezondheidsbewijs. Neem een duidelijke foto mee, mocht uw dier
				te plaatse verloren lopen kunt U onmiddellijk flyers laten maken. Laat het steeds een halsband
				dragen met uw gsm-nummer en vakantieadres. In sommige landen zullen ze het chipnummer misschien
				niet nakijken, niet weten dat het een Belgische registratie is. Of ze krijgen geen gehoor op
				uw huistelefoon.
				<br /><br />
				<span class="font-bold">Een oppas</span><br />
				De beste formule voor een poes is ze thuislaten en een oppas vragen die in uw huis slaapt. Een
				bewoonde woning zorgt meteen ook voor meer veiligheid. Indien onmogelijk zoek dan iemand die
				ze dagelijks enkele uurtjes gezelschap kan houden. Laat de poes niet buiten, ook al is ze het
				gewend. Het is een jaarlijks weerkerend fenomeen: iemand gaat voeding geven, de katten voelen
				zich achtergelaten, gaan op zoek naar hun baasje en lopen verloren Geef alle nodige informatie
				aan de persoon die voor uw dier zorgt. Het chipnummer, de voedingsgewoontes, gegevens van uw
				dierenarts, telefoonnummer van de dichtstbijzijnde dierenklinieken en van de weekendwachtdienst
				van dierenartsen.
				<br /><br />
				<span class="font-bold">Opvang</span><br />
				Een goed dierenpension is snel volgeboekt. Reserveer minstens een half jaar vooraf voor de drukke
				vakantieperiode. Ga zelf kijken naar de dierenverblijven en ter plaatse informatie inwinnen.
				Enkel u kunt vaststellen of het een geschikt verblijf is voor uw dier.
				<br /><br />
				Een asiel is geen pension. Asielen zijn al overbelast tijdens de vakantieperiode, er is plaatsgebrek,
				ze kunnen de pensiongasten niet voldoende ruimte en aandacht geven.
			</p>
