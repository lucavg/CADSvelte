				zien, muisjes, nachtvlinders, ze lopen te ver weg en vinden hun huis niet terug. Als ze op straat
				kunnen, worden ze verblind door de lampen van auto's.<br />
				Eventuele nachtelijke gevechten zorgen voor burenhinder.<br />
				Een kat heeft een ingebouwde wekker! Ze kennen snel wat er om welke tijd gebeurt, dus ook eten.
