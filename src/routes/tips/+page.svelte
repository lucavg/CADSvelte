				Als u uw poes een halsbandje laat dragen zorg ervoor dat het elastisch is. Zoniet kunnen ze zich
				verhangen wanneer ze er ergens mee blijven hangen. Doe het niet te los om. Je mag er 1 vinger
				kunnen tussen steken. We vinden poezen die met hun poot door het bandje raken, waardoor het onder
				de oksel komt en ernstige verwondingen veroorzaakt. Metalen kokertjes draaien los bij het lopen,
				de onderste helft met het adres wordt verloren. Plastic kokertjes zijn degelijker.
